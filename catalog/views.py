def search(request):

    results = []

    if request.method == "GET":

        query = request.GET.get('search')

        if query == '':

            query = 'None'

        results = Book.objects.filter(Q(genre__name__icontains=query) )

    return render(request, 'book_search.html', {'query': query, 'results': results})

